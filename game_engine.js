function randomNum(min,max){
    return Math.floor(Math.random() * (max - min + 1)) + min
}

function moveAnt(obj){
    let array_ant = obj.coordinates
    setInterval(()=>{
        for(let item = 0;item<array_ant.length;item++){
            array_ant[item]["x"] = randomNum(50,500)
            array_ant[item]["y"] = randomNum(50,500)
            array_ant[item]["angle"] = randomNum(0,360)
        }
        fs.writeFile(full_path("/ant_basic/ant.json"),JSON.stringify(obj),(err)=>{
            
        })
    },200)
}

function cercitMove(obj){
    let array_ant = obj.coordinates
    let angle = 0
    let radius = 150
    let dx = 200
    let dy = 300
    let delta_angle = 180
    setInterval(()=>{
        for(let item = 0;item<array_ant.length;item++){
            if(angle === 360){
                angle = 0
            }
            let gradus = angle*Math.PI/180
            array_ant[item]["x"] = radius*Math.cos(gradus)+dx + randomNum(50,250)
            array_ant[item]["y"] = radius*Math.sin(gradus)+dy + randomNum(50,250)
            array_ant[item]["angle"] = angle + delta_angle
            angle++
        }
        fs.writeFile(full_path("/ant_basic/ant.json"),JSON.stringify(obj),(err)=>{
            
        })
    },500)
}

